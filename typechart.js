const ALL_TYPES = Object.keys(TYPE_CHART);

// 攻撃タイプ → 防御タイプ(複合)への倍率を返す
// immuneTypes: 特性で無効化できるタイプの配列 (例: ["じめん"], ["ほのお","じめん"])
// resistTypes: 特性で半減できるタイプの配列 (例: ["ほのお","こおり"])
function getEffectiveness(atkType, defTypes, immuneTypes = [], resistTypes = []) {
  if (immuneTypes.includes(atkType)) return 0;
  let mult = 1.0;
  for (const dt of defTypes) {
    if (TYPE_CHART[atkType]["無効"].includes(dt))      { mult *= 0;   }
    else if (TYPE_CHART[atkType]["抜群"].includes(dt)) { mult *= 2;   }
    else if (TYPE_CHART[atkType]["半減"].includes(dt)) { mult *= 0.5; }
  }
  if (resistTypes.includes(atkType)) mult *= 0.5;
  return mult;
}